CREATE TABLE `Thông tin khách tham dự` (
        `Họ` VARCHAR(255) NOT NULL,
        `Tên` VARCHAR(255) NOT NULL,
        `Email` VARCHAR(255) NOT NULL,
        `Số người tham dự` VARCHAR(100) NOT NULL,
        `Lời nhắn` VARCHAR(100) NOT NULL,
        `Điểm danh` VARCHAR(50) NOT NULL
    ) ENGINE = 'MyISAM'